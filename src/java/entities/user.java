public class user {
    private int iduser;
    private String user;
    private String pass;

    public user() {
    }

    public user(String user, String pass) {
        this.user = user;
        this.pass = pass;
    }

    public int getIduser() {
        return iduser;
    }

    public void setIduser(int iduser) {
        this.iduser = iduser;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }
}